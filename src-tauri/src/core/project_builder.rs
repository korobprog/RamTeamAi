use serde::{Deserialize, Serialize};
use std::{
    fs,
    path::{Component, Path, PathBuf},
};
use thiserror::Error;
use time::{format_description::well_known::Rfc3339, OffsetDateTime};

#[derive(Debug, Error)]
pub enum ProjectBuilderError {
    #[error("build requires explicit user confirmation")]
    NotConfirmed,
    #[error("workspace path is empty")]
    EmptyWorkspacePath,
    #[error("workspace file path is invalid")]
    InvalidWorkspaceFilePath,
    #[error("workspace file already exists: {0}")]
    WorkspaceFileExists(String),
    #[error("required workspace files were not created")]
    MissingRequiredWorkspaceFiles,
    #[error("filesystem error: {0}")]
    Io(#[from] std::io::Error),
    #[error("time format error: {0}")]
    TimeFormat(#[from] time::error::Format),
}

#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct PlanArtifact {
    pub id: String,
    pub title: String,
    pub stack: Vec<String>,
    pub steps: Vec<String>,
    pub project_tree: String,
    pub status: String,
    pub edited: bool,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct BuildResult {
    pub phase: String,
    pub root_path: String,
    pub files: Vec<String>,
    pub skipped: bool,
    pub message: String,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct WorkspaceInitResult {
    pub root_path: String,
    pub files: Vec<String>,
    pub created_files: Vec<String>,
    pub existing_files: Vec<String>,
    pub message: String,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct WorkspaceWriteResult {
    pub path: String,
    pub created: bool,
    pub overwritten: bool,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct WorkspaceReadResult {
    pub path: String,
    pub content: String,
    pub exists: bool,
}

// Directories that never carry useful project source for the agents, so we keep
// them out of the snapshot to save context tokens and time.
const SNAPSHOT_SKIP_DIRS: [&str; 6] = [".git", "node_modules", "target", "dist", "build", ".next"];
// Cap so a large repo can never blow up the model context.
const SNAPSHOT_MAX_FILES: usize = 200;
const SNAPSHOT_MAX_FILE_BYTES: u64 = 256 * 1024;

pub fn build_project(
    artifact: &PlanArtifact,
    confirmed: bool,
    base_dir: &Path,
    workspace_path: Option<&Path>,
) -> Result<BuildResult, ProjectBuilderError> {
    if !confirmed {
        return Err(ProjectBuilderError::NotConfirmed);
    }

    let root = workspace_path
        .map(PathBuf::from)
        .unwrap_or_else(|| base_dir.join(slugify(&artifact.title)));
    if root.as_os_str().is_empty() {
        return Err(ProjectBuilderError::EmptyWorkspacePath);
    }

    let init = init_workspace(&root)?;
    let mut files = init.files;

    let mut readme_created_files = Vec::new();
    write_if_missing(
        &root,
        "README.md",
        &format!(
            "# {}\n\nGenerated by RamTeamAi Project Builder.\n\nРабочие файлы системы:\n\n- `MEMORY.md` — память проекта.\n- `PLAN.md` — текущий план.\n- `docs/plan.md` — build-снимок плана.\n",
            artifact.title
        ),
        &mut files,
        &mut readme_created_files,
    )?;

    fs::create_dir_all(root.join("docs"))?;
    fs::write(root.join("PLAN.md"), render_plan(artifact))?;
    fs::write(root.join("docs/plan.md"), render_plan(artifact))?;
    if !files.iter().any(|file| file == "PLAN.md") {
        files.push("PLAN.md".into());
    }
    if !files.iter().any(|file| file == "docs/plan.md") {
        files.push("docs/plan.md".into());
    }
    fs::write(root.join("IMPLEMENTATION.md"), render_implementation_plan(artifact))?;
    if !files.iter().any(|file| file == "IMPLEMENTATION.md") {
        files.push("IMPLEMENTATION.md".into());
    }

    if requires_tauri_react_scaffold(artifact) {
        let mut scaffold_created_files = Vec::new();
        for (relative_path, content) in tauri_react_scaffold_files(artifact) {
            write_if_missing(
                &root,
                relative_path,
                &content,
                &mut files,
                &mut scaffold_created_files,
            )?;
        }
    }

    Ok(BuildResult {
        phase: "scaffold".into(),
        root_path: root.display().to_string(),
        files,
        skipped: false,
        message: "Проект записан в выбранную рабочую папку. MEMORY.md сохранён без перезаписи, PLAN.md обновлён текущим планом.".into(),
    })
}

pub fn init_workspace(root_path: &Path) -> Result<WorkspaceInitResult, ProjectBuilderError> {
    if root_path.as_os_str().is_empty() {
        return Err(ProjectBuilderError::EmptyWorkspacePath);
    }

    fs::create_dir_all(root_path)?;

    let mut files = Vec::new();
    let mut created_files = Vec::new();
    let mut existing_files = Vec::new();
    let created_at = OffsetDateTime::now_utc().format(&Rfc3339)?;

    for dir in ["docs", "src", "tests", "assets"] {
        fs::create_dir_all(root_path.join(dir))?;
        write_if_missing(root_path, &format!("{dir}/.gitkeep"), "", &mut files, &mut created_files)?;
    }

    write_if_missing(root_path, "MEMORY.md", &memory_template(&created_at), &mut files, &mut created_files)?;
    write_if_missing(root_path, "PLAN.md", &plan_template(&created_at), &mut files, &mut created_files)?;

    if !root_path.join("MEMORY.md").is_file() || !root_path.join("PLAN.md").is_file() {
        return Err(ProjectBuilderError::MissingRequiredWorkspaceFiles);
    }

    existing_files.extend(
        files
            .iter()
            .filter(|file| !created_files.contains(file))
            .cloned(),
    );

    Ok(WorkspaceInitResult {
        root_path: root_path.display().to_string(),
        files,
        created_files,
        existing_files,
        message: "Рабочая папка инициализирована: созданы структура, MEMORY.md и PLAN.md без перезаписи существующих файлов.".into(),
    })
}

pub fn write_workspace_file(
    root_path: &Path,
    relative_path: &str,
    content: &str,
    overwrite: bool,
) -> Result<WorkspaceWriteResult, ProjectBuilderError> {
    if root_path.as_os_str().is_empty() {
        return Err(ProjectBuilderError::EmptyWorkspacePath);
    }

    let relative = safe_relative_path(relative_path)?;
    let path = root_path.join(&relative);
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent)?;
    }

    let existed = path.exists();
    if existed && !overwrite {
        return Err(ProjectBuilderError::WorkspaceFileExists(relative.display().to_string()));
    }

    fs::write(&path, content)?;
    Ok(WorkspaceWriteResult {
        path: relative.display().to_string().replace('\\', "/"),
        created: !existed,
        overwritten: existed,
    })
}

pub fn read_workspace_file(
    root_path: &Path,
    relative_path: &str,
) -> Result<WorkspaceReadResult, ProjectBuilderError> {
    if root_path.as_os_str().is_empty() {
        return Err(ProjectBuilderError::EmptyWorkspacePath);
    }

    let relative = safe_relative_path(relative_path)?;
    let path = root_path.join(&relative);
    let normalized = relative.display().to_string().replace('\\', "/");

    if !path.is_file() {
        return Ok(WorkspaceReadResult {
            path: normalized,
            content: String::new(),
            exists: false,
        });
    }

    let content = fs::read_to_string(&path)?;
    Ok(WorkspaceReadResult {
        path: normalized,
        content,
        exists: true,
    })
}

pub fn list_workspace_files(root_path: &Path) -> Result<Vec<String>, ProjectBuilderError> {
    if root_path.as_os_str().is_empty() {
        return Err(ProjectBuilderError::EmptyWorkspacePath);
    }
    if !root_path.is_dir() {
        return Ok(Vec::new());
    }

    let mut files = Vec::new();
    collect_workspace_files(root_path, root_path, &mut files)?;
    files.sort();
    Ok(files)
}

fn collect_workspace_files(
    root: &Path,
    dir: &Path,
    files: &mut Vec<String>,
) -> Result<(), ProjectBuilderError> {
    if files.len() >= SNAPSHOT_MAX_FILES {
        return Ok(());
    }

    let mut entries: Vec<_> = fs::read_dir(dir)?.filter_map(Result::ok).collect();
    entries.sort_by_key(|entry| entry.file_name());

    for entry in entries {
        if files.len() >= SNAPSHOT_MAX_FILES {
            break;
        }

        let path = entry.path();
        let file_type = entry.file_type()?;
        let name = entry.file_name().to_string_lossy().to_string();

        if file_type.is_dir() {
            if SNAPSHOT_SKIP_DIRS.contains(&name.as_str()) {
                continue;
            }
            collect_workspace_files(root, &path, files)?;
            continue;
        }

        if !file_type.is_file() {
            continue;
        }
        if name == ".gitkeep" {
            continue;
        }
        if entry.metadata().map(|meta| meta.len()).unwrap_or(0) > SNAPSHOT_MAX_FILE_BYTES {
            continue;
        }

        if let Ok(relative) = path.strip_prefix(root) {
            files.push(relative.display().to_string().replace('\\', "/"));
        }
    }

    Ok(())
}

fn safe_relative_path(value: &str) -> Result<PathBuf, ProjectBuilderError> {
    let path = Path::new(value.trim());
    if path.as_os_str().is_empty() {
        return Err(ProjectBuilderError::InvalidWorkspaceFilePath);
    }

    let mut result = PathBuf::new();
    for component in path.components() {
        match component {
            Component::Normal(part) => result.push(part),
            Component::CurDir => {}
            _ => return Err(ProjectBuilderError::InvalidWorkspaceFilePath),
        }
    }

    if result.as_os_str().is_empty() {
        return Err(ProjectBuilderError::InvalidWorkspaceFilePath);
    }
    Ok(result)
}

fn write_if_missing(
    root: &Path,
    relative_path: &str,
    content: &str,
    files: &mut Vec<String>,
    created_files: &mut Vec<String>,
) -> Result<(), ProjectBuilderError> {
    let path = root.join(relative_path);
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent)?;
    }
    if path.exists() {
        if !files.iter().any(|file| file == relative_path) {
            files.push(relative_path.into());
        }
        return Ok(());
    }
    fs::write(path, content)?;
    files.push(relative_path.into());
    created_files.push(relative_path.into());
    Ok(())
}

fn render_plan(artifact: &PlanArtifact) -> String {
    format!(
        "# План\n\n## Стек\n{}\n\n## Шаги\n{}\n",
        artifact
            .stack
            .iter()
            .map(|item| format!("- {item}"))
            .collect::<Vec<_>>()
            .join("\n"),
        artifact
            .steps
            .iter()
            .map(|item| format!("- {item}"))
            .collect::<Vec<_>>()
            .join("\n")
    )
}


fn requires_tauri_react_scaffold(artifact: &PlanArtifact) -> bool {
    let source = format!(
        "{}\n{}\n{}\n{}",
        artifact.title,
        artifact.stack.join("\n"),
        artifact.steps.join("\n"),
        artifact.project_tree
    )
    .to_lowercase();
    (source.contains("tauri") || source.contains("src-tauri"))
        && (source.contains("react") || source.contains("main.tsx") || source.contains("app.tsx"))
}

